# Example 5: Using list methods

numbers = [
    1,
    2,
    3,
    4,
    5,
]

# Inserting and element at a specific index
numbers.insert(2, 10)

# Counting occurrences of a value
count = numbers.count(3)

print("Modified list:", numbers)  # Output: [1,2,10,3,4,5]
print("Count of 3:", count)  # Output: Count 0f 3:
